# Lists
numbers = [1, 2, 3, 4, 5]
print(numbers)
print(numbers[0])
print(numbers[2])

# For loop with list
for num in numbers:
    print(num)

# Dictionaries
person = {"name": "Alice", "age": "30", "city": "NYC"}
print(person)
print(person["name"])
print(person["age"])

# For loop with string
word = "hello"
for char in word:
    print(char)

# Nested structures
data = [1, 2, [3, 4, 5]]
print(data)
print(data[2])
